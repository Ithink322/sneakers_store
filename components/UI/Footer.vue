<script setup lang="ts">
import { usePostsStore } from "@/store/Posts";
const store = usePostsStore();

const activeDropdown = ref<string | null>(null);

const toggleInfoDropdown = () => {
  activeDropdown.value = activeDropdown.value === "info" ? null : "info";
};
const toggleProductsDropdown = () => {
  activeDropdown.value =
    activeDropdown.value === "products" ? null : "products";
};
const toggleShopDropdown = () => {
  activeDropdown.value = activeDropdown.value === "shop" ? null : "shop";
};

const isDropdownActive = (dropdownName: string) => {
  return activeDropdown.value === dropdownName;
};

const email = ref("");
const isValidEmail = ref(true);

const succesMessageIsVisible = ref(false);
const emptyMessageIsVisible = ref(false);

const handleInput = () => {
  emptyMessageIsVisible.value = email.value === "";
  isValidEmail.value = true;
  succesMessageIsVisible.value = false;
};

const submitForm = () => {
  const emailRegex =
    /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)+$/;

  if (email.value) {
    isValidEmail.value = emailRegex.test(email.value);
  } else {
    isValidEmail.value = false;
  }

  if (isValidEmail.value && email.value) {
    email.value = "";
    succesMessageIsVisible.value = true;
    emptyMessageIsVisible.value = false;
  } else if (email.value === "") {
    succesMessageIsVisible.value = false;
    isValidEmail.value = true;
    emptyMessageIsVisible.value = true;
  } else {
    succesMessageIsVisible.value = false;
    emptyMessageIsVisible.value = false;
    isValidEmail.value = false;
  }
};
</script>

